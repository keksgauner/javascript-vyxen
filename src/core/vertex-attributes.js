
/**
 * Collection of common vertex attribute names.
 */
class VertexAttributes
{
    static get POSITION() {
        return 'vertex';
    }

    static get NORMAL() {
        return 'normal';
    }

    static get UV() {
        return 'uv';
    }

    static get COLOR() {
        return 'color';
    }
}

export {
    VertexAttributes
};